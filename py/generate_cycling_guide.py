import os
import re
from supertonic import TTS
from ru_preprocessor import RussianTTSPreprocessor # Используем наш препроцессор

def clean_markdown_and_symbols(text: str) -> str:
    # 1. Удаляем ссылки типа [[15]]
    text = re.sub(r'\[\[\d+\]\]', '', text)
    # 2. Удаляем маркеры заголовков и списков (#, -, *, 🔹, ⚠️, ✅, 🎯, 📊, 🚴♂️💨)
    text = re.sub(r'[#\*🔹⚠️✅🎯📊🚴♂💨\>\|]', '', text)
    # 3. Заменяем "км/ч" на "километров в час"
    text = re.sub(r'\bкм/ч\b', 'километров в час', text)
    # 4. Заменяем "Вт" на "ватт"
    text = re.sub(r'\bВт\b', 'ватт', text)
    # 5. Раскрываем "$1500 на $10 000"
    text = text.replace("$1500", "полторы тысячи долларов")
    text = text.replace("$10 000", "десять тысяч долларов")
    
    # Слегка почистим таблицу, чтобы она звучала связно
    text = text.replace("Тип велосипеда Шоссейник/гравел даёт +3–5 км/ч", "Шоссейник или гравел даёт плюс 3-5 километров в час")
    text = text.replace("Давление в шинах Оптимальное давление", "Оптимальное давление в шинах")
    text = text.replace("Чистая трансмиссия Смазанная цепь", "Смазанная цепь")
    text = text.replace("Вес Лёгкие колёса", "Лёгкие колёса")
    
    # 6. Убираем лишние пробелы и пустые строки
    lines = [line.strip() for line in text.split('\n') if line.strip()]
    return " ".join(lines)


def main():
    os.makedirs("results", exist_ok=True)
    tts = TTS(auto_download=False)
    
    # Используем голос F2 для приятного, информативного женского чтения
    style = tts.get_voice_style(voice_name="F2")
    prep = RussianTTSPreprocessor()
    
    raw_text = """
    Владимир, средняя скорость 30 км/ч — это уже уровень подготовленного любителя. Достичь и удерживать её реально, но нужен системный подход. Вот ключевые направления:

    Физическая подготовка и тренировки

    1. Интервальные тренировки — самый эффективный способ повысить мощность. Например: 8–10 повторений по 30–60 секунд интенсивной работы с восстановлением между ними.
    2. Повторы на подъёмах — найдите холм с умеренным уклоном, заезжайте в темпе, спускайтесь накатом и повторяйте 8–10 раз. Это развивает силу и выносливость ног.
    3. Работа над каденсом — стремитесь к 85–95 оборотам в минуту. Темповое педалирование экономичнее силового давления на педали.
    4. Регулярность — 2–3 поездки в неделю по 1–2 часа позволяют выйти на 20–25 км/ч за несколько месяцев, а 25–30 км/ч — при целенаправленной работе.

    Техника и аэродинамика

    Аэропосадка: согните локти, опустите корпус ближе к рулю — это снижает сопротивление воздуха, которое на скоростях выше 16 км/ч становится главным тормозящим фактором.
    Равномерное педалирование: избегайте рывков. Плавное круговое усилие эффективнее толчков.
    Контактные педали или туклипсы: фиксация стопы позволяет задействовать мышцы на всём цикле педалирования и прибавляет 2–4 км/ч к средней.

    Оборудование и настройка велосипеда

    Шоссейник или гравел даёт плюс 3-5 километров в час к средней по асфальту по сравнению с МТБ с шипами.
    Оптимальное давление в шинах снижает сопротивление качению.
    Смазанная цепь и отрегулированные переключатели экономят до 5–10 ватт мощности.
    Лёгкие колёса и рама улучшают разгон, но на ровной трассе аэродинамика важнее веса.

    Не гонитесь за дорогим апгрейдом: переход с велосипеда за $1500 на $10 000 даст минимальный прирост любителю без улучшения техники.

    Тактика езды

    Езда в группе: чередование тяги и сидение в колесе снижает энергозатраты на 20–30%.
    Планирование маршрута: в ветреный день стартуйте против ветра, чтобы вернуться с попутным, когда устанете.
    Минимизация остановок: каждое торможение и разгон съедает среднюю. Предсказывайте светофоры, катитесь накатом.

    Питание и восстановление

    Пейте 150–250 мл жидкости каждые 15–20 минут, принимайте углеводы на длительных заездах — даже 2% обезвоживания снижают производительность.
    Не забывайте про отдых: рост скорости происходит во время восстановления, а не самой тренировки.

    Короткий чек-лист на старт:
    Отрегулируйте давление в шинах под покрытие.
    Потренируйте аэропосадку 5–10 минут за поездку.
    Добавьте 1 интервальную сессию в неделю.
    Следите за каденсом — держите 85 оборотов в минуту.
    Катайтесь в группе хотя бы раз в 2 недели.

    Упорство и системность дадут результат: большинство велосипедистов выходят на 25–30 км/ч за 1–2 сезона регулярных тренировок. Удачи на трассе!
    """
    
    clean_text = clean_markdown_and_symbols(raw_text)
    final_text = prep.apply_rules(clean_text)
    
    print("Текст подготовлен, начинаем генерацию длинного аудио...")
    print(f"Примерный объем текста: {len(final_text)} символов.")
    
    wav, dur = tts.synthesize(text=final_text, lang="ru", voice_style=style, total_steps=12, speed=1.15)
    
    output_path = "results/cycling_guide_female.wav"
    tts.save_audio(wav, output_path)
    
    print(f"Готово! Сгенерировано {dur[0]:.2f} секунд аудио.")
    print(f"Файл сохранен по пути: {os.path.abspath(output_path)}")

if __name__ == "__main__":
    main()
